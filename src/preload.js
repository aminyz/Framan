const { contextBridge, ipcRenderer } = require('electron');
contextBridge.exposeInMainWorld('api', {
  getTasks:      ()          => ipcRenderer.invoke('get-tasks'),
  addTask:       d           => ipcRenderer.invoke('add-task',d),
  toggleTask:    id          => ipcRenderer.invoke('toggle-task',id),
  deleteTask:    id          => ipcRenderer.invoke('delete-task',id),
  getCalDate:    date        => ipcRenderer.invoke('get-cal-date',date),
  getCalRange:   (s,e)       => ipcRenderer.invoke('get-cal-range',{s,e}),
  addCalTask:    d           => ipcRenderer.invoke('add-cal-task',d),
  toggleCal:     id          => ipcRenderer.invoke('toggle-cal',id),
  deleteCal:     id          => ipcRenderer.invoke('delete-cal',id),
  getMonth:      ym          => ipcRenderer.invoke('get-month',ym),
  getDateRangeStats: dates   => ipcRenderer.invoke('get-date-range-stats',dates),
  getDayReport:  date        => ipcRenderer.invoke('get-day-report',date),
  addSession:    d           => ipcRenderer.invoke('add-session',d),
  getStats:      ()          => ipcRenderer.invoke('get-stats'),
  clearData:     ()          => ipcRenderer.invoke('clear-data'),
  getAnalytics:  days        => ipcRenderer.invoke('get-analytics',days),
  getGamif:      ()          => ipcRenderer.invoke('get-gamif'),
  getWeeklyMin:  ()          => ipcRenderer.invoke('get-weekly-min'),
  taskXP:        ()          => ipcRenderer.invoke('task-xp'),
  getNotes:      ()          => ipcRenderer.invoke('get-notes'),
  addNote:       d           => ipcRenderer.invoke('add-note',d),
  updateNote:    d           => ipcRenderer.invoke('update-note',d),
  deleteNote:    id          => ipcRenderer.invoke('delete-note',id),
  getFeeds:      ()          => ipcRenderer.invoke('get-feeds'),
  addFeed:       d           => ipcRenderer.invoke('add-feed',d),
  deleteFeed:    id          => ipcRenderer.invoke('delete-feed',id),
  fetchFeed:     d           => ipcRenderer.invoke('fetch-feed',d),
  getGoal:       ()          => ipcRenderer.invoke('get-goal'),
  setGoal:       min         => ipcRenderer.invoke('set-goal',min),
  notify:        o           => ipcRenderer.invoke('notify',o),
  testNotify:    ()          => ipcRenderer.invoke('test-notify'),
  reschedNotifs: ()          => ipcRenderer.invoke('resched-notifs'),
  openURL:       url         => ipcRenderer.invoke('open-url',url),
});
